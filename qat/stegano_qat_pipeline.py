# ── 0.14 Eval loop (PSNR / SSIM over full dataset) ──────────────────────────
def eval_dataset(encoder, decoder, loader, psnr_fn, ssim_fn, device,
                 label="Model"):
    """Chạy qua toàn bộ loader, trả về dict chỉ số trung bình."""
    encoder.eval(); decoder.eval()
    acc = {"PSNR_C": 0., "SSIM_C": 0., "PSNR_S": 0., "SSIM_S": 0.}
    n = 0
    with torch.no_grad():
        for cover in loader:
            cover  = cover.to(device)
            secret = cover[torch.randperm(cover.size(0))].to(device)
            stego  = encoder(cover, secret)
            rev    = decoder(stego)
            pc, sc = calc_metrics(stego, cover,  psnr_fn, ssim_fn)
            ps, ss = calc_metrics(rev,   secret, psnr_fn, ssim_fn)
            acc["PSNR_C"] += pc; acc["SSIM_C"] += sc
            acc["PSNR_S"] += ps; acc["SSIM_S"] += ss
            n += 1
    res = {k: v / max(n, 1) for k, v in acc.items()}
    print(f"\n[{label}] ── Đánh giá toàn dataset ({n} batches) ──")
    print(f"  Stego  : PSNR={res['PSNR_C']:.2f} dB | SSIM={res['SSIM_C']:.4f}")
    print(f"  Recover: PSNR={res['PSNR_S']:.2f} dB | SSIM={res['SSIM_S']:.4f}")
    return res

