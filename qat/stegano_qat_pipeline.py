"""
Yêu cầu:
  A. Depthwise 9 Banks:
     - Đọc depthwise.mem từ Cell 4, lọc bỏ dead/beef markers.
     - Sắp xếp lại theo 9 Banks (mỗi bank = 1 vị trí không gian kernel 3×3).
     - Mỗi hàng 64-bit = 4 channels (Little Endian: IC3_IC2_IC1_IC0).
     - Pad số channel lên bội số 16 (hardware alignment).
     - Xuất ra depthwise_9banks.mem.

  B. Pointwise 16 Banks:
     - Đọc pointwise.mem từ Cell 4.
     - Phân tách Weight [OC][IC] và Bias theo topology mô hình.
     - Phân bổ theo 16 Banks (OC % 16 == bank_id).
     - Weight: pad IC lên bội số 16, gom 4 IC / hàng 64-bit LE.
     - Bias  : định dạng 000000000000<bias_hex> (64-bit, 3 words dump + 1 bias).
     - Xuất ra pointwise_16banks.mem.
"""

import math as _math

# ══════════════════════════════════════════════════════════════════════════════
# PHẦN A – DEPTHWISE 9 BANKS
# ══════════════════════════════════════════════════════════════════════════════

def _build_dw_layer_map(model: nn.Module):
    """Tự động lấy (tên_rút_gọn, in_channels) của từng DW Conv theo thứ tự."""
    lmap = []
    for name, m in model.named_modules():
        if isinstance(m, nn.Conv2d) and m.groups > 1 and \
           m.kernel_size == (3, 3):
            # Rút tên ngắn: "head.0.depthwise" → "head"
            short = name.split(".")[0]
            lmap.append((short, m.in_channels))
    return lmap

def rearrange_depthwise_9banks(input_filepath: str, output_filepath: str,
                                layer_map: list):
    """
    Đọc depthwise.mem (flat 16-bit hex, 1 word/dòng),
    sắp xếp theo 9 Banks kernel 3×3, pad channel lên bội 16,
    ghi ra 64-bit Little Endian (4 channels / hàng).
    """
    print(f"\n[Cell 4.5-A] Đọc depthwise.mem: {input_filepath}")

    with open(input_filepath, "r") as f:
        lines = f.read().splitlines()

    # Lọc marker, comment, dòng trống
    raw_data = []
    for line in lines:
        s = line.strip().lower()
        if not s or s.startswith("//") or s in ("dead", "beef"):
            continue
        raw_data.append(s)
    print(f"  Words 16-bit (sau lọc): {len(raw_data)}")

    # Phân tách theo layer; PyTorch shape [C,1,3,3] → flatten: ch0_sp0..sp8, ch1_sp0..
    parsed_layers = []
    ptr = 0
    for name, c in layer_map:
        num_w = c * 9
        chunk = raw_data[ptr: ptr + num_w]
        ptr  += num_w
        if len(chunk) != num_w:
            print(f"  ⚠ LỖI layer '{name}': cần {num_w}, còn {len(chunk)}. Dừng.")
            return None
        # Nhóm theo 9 vị trí không gian: spatial[sp] = [ch0, ch1, ..., chC-1]
        spatial = [[] for _ in range(9)]
        for ch in range(c):
            for sp in range(9):
                spatial[sp].append(chunk[ch * 9 + sp])
        parsed_layers.append({"name": name, "c": c, "spatial": spatial})

    if ptr != len(raw_data):
        print(f"  ⚠ Còn {len(raw_data) - ptr} words chưa đọc (layer_map thiếu?).")

    # Ghi 9 Banks
    os.makedirs(os.path.dirname(os.path.abspath(output_filepath)), exist_ok=True)
    total_rows = 0
    with open(output_filepath, "w") as f_out:
        for sp in range(9):
            ky, kx = sp // 3, sp % 3
            f_out.write(f"// =========================================\n")
            f_out.write(f"// BANK POSITION ({ky}, {kx})\n")
            f_out.write(f"// =========================================\n")
            for layer in parsed_layers:
                c        = layer["c"]
                w_list   = layer["spatial"][sp]
                padded_c = _math.ceil(c / 16) * 16
                w_padded = w_list + ["0000"] * (padded_c - c)
                f_out.write(f"// Layer: {layer['name']} (C={c}, padded={padded_c})\n")
                for i in range(0, padded_c, 4):
                    ic0, ic1, ic2, ic3 = (w_padded[i],   w_padded[i+1],
                                          w_padded[i+2], w_padded[i+3])
                    f_out.write(f"{ic3}{ic2}{ic1}{ic0}\n")
                    total_rows += 1

    # Tóm tắt
    print(f"\n  {'Layer':<16} {'C':>5} {'Padded':>8} {'Rows/bank':>10}")
    print("  " + "-"*42)
    for layer in parsed_layers:
        pc = _math.ceil(layer['c'] / 16) * 16
        print(f"  {layer['name']:<16} {layer['c']:>5} {pc:>8} {pc//4:>10}")
    print(f"\n  Tổng hàng 64-bit : {total_rows}")
    print(f"  Xuất             : {output_filepath}")
    return output_filepath

