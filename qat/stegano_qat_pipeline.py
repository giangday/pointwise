def float_to_hex_lines(tensor: torch.Tensor, scale: float = Q_SCALE):
    """Tensor float → list các chuỗi hex 16-bit (two's complement)."""
    arr = tensor.detach().cpu().numpy().flatten()
    arr_i = np.clip(np.round(arr * scale), INT16_MIN, INT16_MAX).astype(np.int32)
    return [f"{v & 0xFFFF:04x}" for v in arr_i]

def export_mem_files(model: nn.Module, output_dir: str,
                     insert_deadbeef_after_head: bool = True):
    """
    Duyệt Fused Encoder, ghi:
      depthwise.mem – trọng số tất cả Depthwise Conv
      pointwise.mem – trọng số tất cả Pointwise Conv (kèm bias nếu có)

    Tham số insert_deadbeef_after_head:
      Chèn 0xDEAD 0xBEEF (alignment marker) sau head depthwise.
    """
    os.makedirs(output_dir, exist_ok=True)
    dw_path = os.path.join(output_dir, "depthwise.mem")
    pw_path = os.path.join(output_dir, "pointwise.mem")

    addr_dw, addr_pw = 0, 0
    map_info = []
    is_head = True  # cờ để chèn alignment marker

    with open(dw_path, "w") as f_dw, open(pw_path, "w") as f_pw:
        for name, module in model.named_modules():

            # ── Depthwise Conv (groups > 1) ────────────────────────────────
            if isinstance(module, nn.Conv2d) and module.groups > 1:
                lines = float_to_hex_lines(module.weight)
                cnt   = len(lines)
                f_dw.write("\n".join(lines) + "\n")
                map_info.append({
                    "file": "DW", "layer": name,
                    "addr_start": addr_dw, "words": cnt,
                    "shape": str(list(module.weight.shape))})
                addr_dw += cnt

                # if insert_deadbeef_after_head and is_head:
                #     f_dw.write("dead\nbeef\n")
                #     map_info.append({
                #         "file": "DW", "layer": "[ALIGN_DEADBEEF]",
                #         "addr_start": addr_dw, "words": 2, "shape": "–"})
                #     addr_dw += 2
                #     is_head = False

            # ── Pointwise Conv (kernel_size == 1 × 1) ─────────────────────
            elif isinstance(module, nn.Conv2d) and \
                 module.kernel_size == (1, 1) and module.groups == 1:
                # Weight
                w_lines = float_to_hex_lines(module.weight)
                cnt_w   = len(w_lines)
                f_pw.write("\n".join(w_lines) + "\n")
