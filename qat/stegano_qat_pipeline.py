# PHẦN B – POINTWISE 16 BANKS
# ══════════════════════════════════════════════════════════════════════════════

def _build_pw_topology(model: nn.Module):
    """
    Tự động xây topology Pointwise từ Fused Encoder.
    Mỗi entry: {"name", "ic", "oc", "has_bias"}
    Chỉ lấy Conv2d kernel_size=1×1, groups=1 (pointwise).
    """
    topology = []
    for name, m in model.named_modules():
        if isinstance(m, nn.Conv2d) and m.kernel_size == (1, 1) \
                and m.groups == 1:
            short = name.split(".")[0]
            topology.append({
                "name":     short,
                "ic":       m.in_channels,
                "oc":       m.out_channels,
                "has_bias": m.bias is not None,
            })
    return topology

def rearrange_pointwise_16banks(input_filepath: str, output_filepath: str,
                                 topology: list):
    """
    Đọc pointwise.mem (flat 16-bit hex),
    phân tách Weight[OC][IC] + Bias theo topology,
    phân bổ vào 16 Banks (OC % 16 == bank_id),
    ghi ra 64-bit LE; bias dạng 000000000000<BIAS>.
    """
    print(f"\n[Cell 4.5-B] Đọc pointwise.mem: {input_filepath}")

    with open(input_filepath, "r") as f:
        lines = f.read().splitlines()

    raw_data = [l.strip().lower() for l in lines
                if l.strip() and not l.strip().startswith("//")]
    print(f"  Words 16-bit (raw): {len(raw_data)}")

    # Phân tách từng layer
    parsed_layers = []
    ptr = 0
    for layer in topology:
        ic, oc = layer["ic"], layer["oc"]
        w_size = oc * ic
        weights_flat = raw_data[ptr: ptr + w_size];  ptr += w_size

        biases = []
        if layer["has_bias"]:
            biases = raw_data[ptr: ptr + oc];  ptr += oc

        # Tổ chức thành [OC][IC]
        weights_2d = []
        for o in range(oc):
            weights_2d.append(weights_flat[o * ic: (o + 1) * ic])

        parsed_layers.append({
            "name":     layer["name"],
            "ic":       ic,
            "oc":       oc,
            "has_bias": layer["has_bias"],
            "weights":  weights_2d,
            "biases":   biases,
        })

    if ptr != len(raw_data):
        print(f"  ⚠ Còn {len(raw_data) - ptr} words chưa đọc.")

    # Ghi 16 Banks
    os.makedirs(os.path.dirname(os.path.abspath(output_filepath)), exist_ok=True)
    total_w_rows = 0
    total_b_rows = 0

    print(f"  Phân bổ vào 16 Banks → {output_filepath}")
    with open(output_filepath, "w") as f_out:
        for bank in range(16):
            f_out.write(f"// ==================================================\n")
            f_out.write(f"// BANK {bank}\n")
            f_out.write(f"// ==================================================\n\n")

            # ── Weights ───────────────────────────────────────────────────────
            f_out.write(f"// --- WEIGHTS ---\n")
            for layer in parsed_layers:
                valid_ocs = [oc for oc in range(layer["oc"])
                             if oc % 16 == bank]
                if not valid_ocs:
                    continue
                f_out.write(f"// Layer: {layer['name']} | IC: {layer['ic']}\n")
                for oc in valid_ocs:
                    f_out.write(f"//   OC: {oc}\n")
                    ic_list  = layer["weights"][oc]
                    c        = layer["ic"]
                    padded_c = _math.ceil(c / 16) * 16
                    padded_ic = ic_list + ["0000"] * (padded_c - c)
                    for i in range(0, padded_c, 4):
                        ic0, ic1, ic2, ic3 = (padded_ic[i],   padded_ic[i+1],
                                              padded_ic[i+2], padded_ic[i+3])
                        f_out.write(f"{ic3}{ic2}{ic1}{ic0}\n")
                        total_w_rows += 1

            f_out.write("\n")

            # ── Biases ────────────────────────────────────────────────────────
            f_out.write(f"// --- BIASES ---\n")
            for layer in parsed_layers:
                if not layer["has_bias"]:
                    continue
                valid_ocs = [oc for oc in range(layer["oc"])
                             if oc % 16 == bank]
                if not valid_ocs:
                    continue
                f_out.write(f"// Layer: {layer['name']}\n")
                for oc in valid_ocs:
                    bval = layer["biases"][oc]
                    # 64-bit: [63:16]=0x000000000000, [15:0]=bias
                    f_out.write(f"000000000000{bval} // OC {oc}\n")
                    total_b_rows += 1

            f_out.write("\n\n")

    print(f"\n  {'Layer':<16} {'IC':>5} {'OC':>5} {'Bias':>6}")
    print("  " + "-"*34)
    for layer in parsed_layers:
        print(f"  {layer['name']:<16} {layer['ic']:>5} {layer['oc']:>5}"
              f"  {'Yes' if layer['has_bias'] else 'No':>5}")
    print(f"\n  Hàng weight 64-bit : {total_w_rows}")
    print(f"  Hàng bias  64-bit  : {total_b_rows}")
    print(f"  Xuất               : {output_filepath}")
    return output_filepath

