Q_FRAC   = 10
Q_SCALE  = float(2 ** Q_FRAC)   # 1024.0
INT16_MIN, INT16_MAX = -32768, 32767

# ── 0.4  FakeQuantize (STE) ─────────────────────────────────────────────────
class FakeQuantizeQ6_10(torch.autograd.Function):
    """Q6.10: scale=1024, range [-32.0, +31.999], STE backward."""
    @staticmethod
    def forward(ctx, x):
        return torch.round(x * Q_SCALE).clamp(INT16_MIN, INT16_MAX) / Q_SCALE
    @staticmethod
    def backward(ctx, grad):
        return grad

def qat_quant(x):
    return FakeQuantizeQ6_10.apply(x)

class QuantIdentity(nn.Module):
    def forward(self, x):
        return qat_quant(x)

# ── 0.5  Kiến trúc chung: SeparableConv2d ───────────────────────────────────
class SeparableConv2d(nn.Module):
    def __init__(self, in_c, out_c, kernel_size=3, stride=1, padding=1):
        super().__init__()
        self.depthwise = nn.Conv2d(in_c, in_c, kernel_size, stride, padding,
                                   groups=in_c, bias=False)
        self.pointwise = nn.Conv2d(in_c, out_c, kernel_size=1, bias=False)

    def forward(self, x):
        return self.pointwise(self.depthwise(x))

# ── 0.6  Kiến trúc QAT: QATSeparableConv2d ──────────────────────────────────
class QATSeparableConv2d(nn.Module):
    """Depthwise+Pointwise với FakeQuantize trọng số và kết quả trung gian.

    Sau khi fuse BN (Cell 3), thuộc tính `bn_fused=True` được set.
    Lúc đó:
      - Pointwise weight đã hấp thụ BN → KHÔNG quantize lại (sẽ sai)
      - Pointwise bias đã hấp thụ BN → truyền thẳng vào F.conv2d
    """
    def __init__(self, in_c, out_c, kernel_size=3, stride=1, padding=1):
        super().__init__()
        self.depthwise = nn.Conv2d(in_c, in_c, kernel_size, stride, padding,
                                   groups=in_c, bias=False)
        self.pointwise = nn.Conv2d(in_c, out_c, kernel_size=1, bias=False)
        self.bn_fused  = False   # set True bởi fuse_model_qat

    def forward(self, x):
        # ── Depthwise: luôn quantize weight (không bao giờ fuse DW+BN) ──────
        w_dw = qat_quant(self.depthwise.weight)
        x = F.conv2d(x, w_dw, None,
                     self.depthwise.stride, self.depthwise.padding,
                     self.depthwise.dilation, self.depthwise.groups)
        x = qat_quant(x)    # quantize activation trung gian

        # ── Pointwise: hành vi khác nhau tuỳ trạng thái fuse ────────────────
        if self.bn_fused:
            # Weight và bias đã hấp thụ BN → dùng trực tiếp, KHÔNG quantize
            x = F.conv2d(x, self.pointwise.weight, self.pointwise.bias,
                         self.pointwise.stride, self.pointwise.padding)
        else:
            # Chế độ QAT bình thường → quantize weight trước khi nhân
            w_pw = qat_quant(self.pointwise.weight)
            b_pw = self.pointwise.bias  # None trước fuse
            x = F.conv2d(x, w_pw, b_pw,
                         self.pointwise.stride, self.pointwise.padding)
        return x

# ── 0.7  Baseline Encoder (Float32) ─────────────────────────────────────────
class Encoder(nn.Module):
    """U-Net Encoder dùng SeparableConv2d thuần Float32."""
    def __init__(self, base_c=16):
        super().__init__()

        def conv_block(in_c, out_c, stride=1):
            return nn.Sequential(
                SeparableConv2d(in_c, out_c, stride=stride),
                nn.BatchNorm2d(out_c),
                nn.LeakyReLU(0.1, inplace=True))

        def conv_block_no_act(in_c, out_c):
            return nn.Sequential(SeparableConv2d(in_c, out_c))

        self.head       = conv_block(6,            base_c)           # 128×128
        self.down1      = conv_block(base_c,       base_c*2, stride=2)  # 64×64
        self.down2      = conv_block(base_c*2,     base_c*4, stride=2)  # 32×32
        self.down3      = conv_block(base_c*4,     base_c*8, stride=2)  # 16×16
        self.bottleneck = conv_block(base_c*8,     base_c*8, stride=2)  #  8×8
        self.upsample   = nn.Upsample(scale_factor=2, mode='nearest')
        self.up1        = conv_block(base_c*8*2,   base_c*4)
        self.up2        = conv_block(base_c*4*2,   base_c*2)
        self.up3        = conv_block(base_c*2*2,   base_c)
        self.up4        = conv_block(base_c*2,     base_c)
        self.tail       = conv_block_no_act(base_c, 3)

    def forward(self, x_cover, x_secret):
        x  = torch.cat([x_cover, x_secret], dim=1)
        h  = self.head(x)
        d1 = self.down1(h)
        d2 = self.down2(d1)
        d3 = self.down3(d2)
        b  = self.bottleneck(d3)
        u1 = self.up1(torch.cat([self.upsample(b),  d3], dim=1))
        u2 = self.up2(torch.cat([self.upsample(u1), d2], dim=1))
        u3 = self.up3(torch.cat([self.upsample(u2), d1], dim=1))
        u4 = self.up4(torch.cat([self.upsample(u3), h],  dim=1))
        return torch.clamp(x_cover + self.tail(u4), -1, 1)

# ── 0.8  Baseline Decoder (Float32) ─────────────────────────────────────────
class Decoder(nn.Module):
    """U-Net Decoder dùng Full Conv Float32."""
    def __init__(self, base_c=16):
        super().__init__()

        def conv_block(in_c, out_c, stride=1):
            return nn.Sequential(
                nn.Conv2d(in_c, out_c, 3, stride, 1, bias=True),
                nn.BatchNorm2d(out_c),
                nn.LeakyReLU(0.1, inplace=True))

        self.head       = conv_block(3,           base_c)
        self.down1      = conv_block(base_c,      base_c*2,  stride=2)
        self.down2      = conv_block(base_c*2,    base_c*4,  stride=2)
        self.down3      = conv_block(base_c*4,    base_c*8,  stride=2)
        self.down4      = conv_block(base_c*8,    base_c*16, stride=2)
        self.bottleneck = conv_block(base_c*16,   base_c*16, stride=2)
        self.upsample   = nn.Upsample(scale_factor=2, mode='bilinear',
                                       align_corners=True)
        self.up1        = conv_block(base_c*16*2, base_c*8)
        self.up2        = conv_block(base_c*8*2,  base_c*4)
        self.up3        = conv_block(base_c*4*2,  base_c*2)
        self.up4        = conv_block(base_c*2*2,  base_c)
        self.up5        = conv_block(base_c*2,    base_c)
        self.tail       = nn.Sequential(
            nn.Conv2d(base_c, 3, 3, padding=1), nn.Tanh())

    def forward(self, x):
        c1 = self.head(x)
        c2 = self.down1(c1)
        c3 = self.down2(c2)
        c4 = self.down3(c3)
        c5 = self.down4(c4)
        b  = self.bottleneck(c5)
        u1 = self.up1(torch.cat([self.upsample(b),  c5], dim=1))
        u2 = self.up2(torch.cat([self.upsample(u1), c4], dim=1))
        u3 = self.up3(torch.cat([self.upsample(u2), c3], dim=1))
        u4 = self.up4(torch.cat([self.upsample(u3), c2], dim=1))
        u5 = self.up5(torch.cat([self.upsample(u4), c1], dim=1))
        return self.tail(u5)

# ── 0.9  QAT Encoder ────────────────────────────────────────────────────────
class QATEncoder(nn.Module):
    """Encoder dùng QATSeparableConv2d + FakeQuantize activations.

    Tên attribute và key state_dict cố tình giữ GIỐNG Baseline Encoder:
      head / down1 / down2 / down3 / bottleneck / up1..up4 / tail
    → load_qat_from_baseline sẽ match được 100% key.
    Điểm khác: các block dùng QATSeparableConv2d thay vì SeparableConv2d,
    thêm ReLU + QuantIdentity (BN key vẫn giữ nguyên).
    """
    def __init__(self, base_c=16):
        super().__init__()

        def qat_block(in_c, out_c, stride=1):
            return nn.Sequential(
                QATSeparableConv2d(in_c, out_c, stride=stride),
                nn.BatchNorm2d(out_c),
                nn.ReLU(inplace=True),
                QuantIdentity())          # quantize activation sau ReLU

        def qat_block_no_act(in_c, out_c):
            """Giống conv_block_no_relu của Baseline: chỉ có SeparableConv.
            Key: tail.0.depthwise.weight, tail.0.pointwise.weight
            """
            return nn.Sequential(QATSeparableConv2d(in_c, out_c))

        # Tên PHẢI khớp với Baseline Encoder để copy weight
        self.head       = qat_block(6,          base_c)
        self.down1      = qat_block(base_c,     base_c*2, stride=2)
        self.down2      = qat_block(base_c*2,   base_c*4, stride=2)
        self.down3      = qat_block(base_c*4,   base_c*8, stride=2)
        self.bottleneck = qat_block(base_c*8,   base_c*8, stride=2)
        self.upsample   = nn.Upsample(scale_factor=2, mode='nearest')
        self.up1        = qat_block(base_c*8*2, base_c*4)
        self.up2        = qat_block(base_c*4*2, base_c*2)
        self.up3        = qat_block(base_c*2*2, base_c)
        self.up4        = qat_block(base_c*2,   base_c)
        # tail giống Baseline: Sequential(QATSeparableConv2d(base_c, 3))
        # key: tail.0.depthwise.weight / tail.0.pointwise.weight
        self.tail       = qat_block_no_act(base_c, 3)

    def forward(self, x_cover, x_secret):
        # Quantize input (mô phỏng pixel 16-bit vào FPGA)
        x_cover  = qat_quant(x_cover)
        x_secret = qat_quant(x_secret)
        x  = torch.cat([x_cover, x_secret], dim=1)
        h  = self.head(x)
        d1 = self.down1(h)
        d2 = self.down2(d1)
        d3 = self.down3(d2)
        b  = self.bottleneck(d3)
        u1 = self.up1(torch.cat([self.upsample(b),  d3], dim=1))
        u2 = self.up2(torch.cat([self.upsample(u1), d2], dim=1))
        u3 = self.up3(torch.cat([self.upsample(u2), d1], dim=1))
        u4 = self.up4(torch.cat([self.upsample(u3), h],  dim=1))

        # Quantize weight lớp tail trước khi chạy (forward thủ công)
        sep = self.tail[0]                              # QATSeparableConv2d
        w_dw = qat_quant(sep.depthwise.weight)
        feat = F.conv2d(u4, w_dw, None,
                        sep.depthwise.stride, sep.depthwise.padding,
                        sep.depthwise.dilation, sep.depthwise.groups)
        feat = qat_quant(feat)
        w_pw = qat_quant(sep.pointwise.weight)
        out  = F.conv2d(feat, w_pw, None,
                        sep.pointwise.stride, sep.pointwise.padding)

        # Quantize output cuối (sau Tanh + residual)
        out = qat_quant(torch.tanh(out))
        return torch.clamp(x_cover + out, -1, 1)

# ── 0.10 Dataset & DataLoader ────────────────────────────────────────────────
class RealImageDataset(Dataset):
    def __init__(self, directory, img_size=128):
        try:
            self.paths = [os.path.join(directory, f)
                          for f in sorted(os.listdir(directory))
                          if f.lower().endswith(('png','jpg','jpeg'))]
        except Exception:
            self.paths = []
        self.transform = transforms.Compose([
            transforms.Resize((img_size, img_size)),
            transforms.ToTensor(),
            transforms.Normalize([0.5]*3, [0.5]*3)])

    def __len__(self):
        return len(self.paths)

    def __getitem__(self, i):
        try:
            return self.transform(Image.open(self.paths[i]).convert("RGB"))
        except Exception:
            return torch.zeros(3, 128, 128)

def get_loaders(cfg):
    train_ds = RealImageDataset(cfg.TRAIN_REAL_DIR, cfg.IMG_SIZE)
    val_ds   = RealImageDataset(cfg.VAL_REAL_DIR,   cfg.IMG_SIZE)
    dl_train = DataLoader(train_ds, batch_size=cfg.BATCH_SIZE,
                          shuffle=True,  num_workers=2, drop_last=True)
    dl_val   = DataLoader(val_ds,   batch_size=cfg.BATCH_SIZE,
                          shuffle=False, num_workers=2, drop_last=True)
    return dl_train, dl_val

# ── 0.11 Metric helpers ──────────────────────────────────────────────────────
def build_metrics(device):
    psnr_fn = PeakSignalNoiseRatio(data_range=1.0).to(device)
    ssim_fn = StructuralSimilarityIndexMeasure(data_range=1.0).to(device)
    return psnr_fn, ssim_fn

def calc_metrics(i1, i2, psnr_fn, ssim_fn):
    i1 = ((i1 + 1) / 2).clamp(0, 1)
    i2 = ((i2 + 1) / 2).clamp(0, 1)
    return psnr_fn(i1, i2).item(), ssim_fn(i1, i2).item()

def denorm_np(t):
    """Tensor [B,C,H,W] → numpy [B,H,W,C] in [0,1]."""
    return ((t + 1) / 2).clamp(0, 1).cpu().permute(0, 2, 3, 1).numpy()

# ── 0.12 HEX / Q6.10 helpers ────────────────────────────────────────────────
def float_to_q6_10(x: torch.Tensor) -> torch.Tensor:
    """Float tensor (bất kỳ device) → int16 tensor trên CPU (Q6.10)."""
    return (torch.round(x.detach().float().cpu() * Q_SCALE)
              .clamp(INT16_MIN, INT16_MAX)
              .to(torch.int16))

def save_hex(tensor_int16: torch.Tensor, filepath: str):
    """Lưu int16 tensor ra file hex 4 ký tự / dòng (two's complement).
    Tự động chuyển về CPU nếu tensor đang trên GPU.
    """
    os.makedirs(os.path.dirname(os.path.abspath(filepath)), exist_ok=True)
    arr = tensor_int16.detach().cpu().contiguous().view(-1).numpy().astype(np.int32)
    with open(filepath, "w") as f:
        for v in arr:
            f.write(f"{v & 0xFFFF:04x}\n")

def export_io_hex(cover, secret, stego, recover, out_dir, prefix=""):
    """Xuất 4 file HEX cho 1 cặp ảnh mẫu."""
    os.makedirs(out_dir, exist_ok=True)
    p = lambda name: os.path.join(out_dir, f"{prefix}{name}.hex")
    save_hex(float_to_q6_10(cover.cpu()),   p("input_cover"))
    save_hex(float_to_q6_10(secret.cpu()),  p("input_secret"))
    save_hex(float_to_q6_10(stego.cpu()),   p("output_stego"))
    save_hex(float_to_q6_10(recover.cpu()), p("output_recover"))
    print(f"  [HEX] Xuất 4 file hex vào: {out_dir}")

