# ── 3.1  Hàm fuse_conv_bn ────────────────────────────────────────────────────
def fuse_conv_bn_into_conv(conv: nn.Conv2d, bn: nn.BatchNorm2d):
    """
    Gộp BN vào Conv (Pointwise):
        w_fused = w * (gamma / sqrt(var+eps))
        b_fused = beta + (b_conv - mean) * (gamma / sqrt(var+eps))
    Trả về Conv2d mới có bias, BN đã được "hấp thụ".
    """
    with torch.no_grad():
        gamma    = bn.weight
        beta     = bn.bias
        mean     = bn.running_mean
        var_sqrt = torch.sqrt(bn.running_var + bn.eps)
        scale    = (gamma / var_sqrt)

        b_conv = conv.bias if conv.bias is not None \
                 else torch.zeros_like(mean)

        w_fused = conv.weight * scale.view(-1, 1, 1, 1)
        b_fused = beta + (b_conv - mean) * scale

    # Tạo fused conv trên cùng device với conv gốc
    target_device = conv.weight.device
    fused_conv = nn.Conv2d(
        conv.in_channels, conv.out_channels,
        kernel_size=conv.kernel_size,
        stride=conv.stride, padding=conv.padding,
        groups=conv.groups, bias=True).to(target_device)
    fused_conv.weight.data.copy_(w_fused)
    fused_conv.bias.data.copy_(b_fused)
    return fused_conv

def fuse_model_qat(model: nn.Module) -> nn.Module:
    """
    Duyệt các nn.Sequential chứa (QATSeparableConv2d, BatchNorm2d, ...):
      - Fuse BN vào pointwise Conv của QATSeparableConv2d.
      - Set flag `bn_fused = True` để forward không quantize weight fused.
      - Thay BN bằng nn.Identity().
    """
    fused_count = 0
    for name, seq in model.named_modules():
        if not isinstance(seq, nn.Sequential) or len(seq) < 2:
            continue
        if isinstance(seq[0], QATSeparableConv2d) and \
           isinstance(seq[1], nn.BatchNorm2d):
            seq[0].pointwise = fuse_conv_bn_into_conv(seq[0].pointwise, seq[1])
            seq[0].bn_fused  = True      # ← báo forward dùng weight fused trực tiếp
            seq[1] = nn.Identity()
            fused_count += 1
    print(f"  [Fuse] Đã gộp {fused_count} khối (Conv+BN → FusedConv).")
    return model

