def load_baseline_weights(enc, dec, enc_path, dec_path, device):
    for model, path, name in [(enc, enc_path, "Encoder"),
                               (dec, dec_path, "Decoder")]:
        if os.path.exists(path):
            raw = torch.load(path, map_location=device)
            # Hỗ trợ cả checkpoint (dict có key 'enc'/'dec') lẫn state_dict thuần
            if isinstance(raw, dict):
                for key in ('enc', 'encoder', 'dec', 'decoder', 'state_dict'):
                    if key in raw:
                        raw = raw[key]; break
            model.load_state_dict(raw, strict=False)
            print(f"  [Load] {name} ← {path}")
        else:
            print(f"  [WARN] Không tìm thấy {path} – dùng random weights để demo.")

