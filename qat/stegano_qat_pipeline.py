# ── 5.1  Đăng ký hooks ───────────────────────────────────────────────────────
def register_activation_hooks(model: nn.Module, storage: dict,
                               target_types=None):
    """
    Gắn forward hook vào các layer thuộc target_types.
    Mặc định: Conv2d, ReLU, LeakyReLU, Tanh, QATSeparableConv2d, BatchNorm2d.
    Trả về list hooks (để remove sau khi dùng).
    """
    if target_types is None:
        target_types = (
            nn.Conv2d, nn.ReLU, nn.LeakyReLU, nn.Tanh,
            QATSeparableConv2d, nn.BatchNorm2d, nn.Identity)

    handles = []

    def make_hook(layer_name):
        def hook(module, inp, output):
            # Chỉ lưu tensor (bỏ qua Identity output rỗng)
            if isinstance(output, torch.Tensor):
                storage[layer_name] = output.detach().cpu().float()
        return hook

    for name, module in model.named_modules():
        if isinstance(module, target_types):
            safe = name.replace(".", "_") if name else module.__class__.__name__
            handles.append(module.register_forward_hook(make_hook(safe)))

    return handles

