# 3. SAVE HEX FILE
# ==============================
def save_hex(tensor, filename):
    """Lưu tensor int16 ra file hex chuẩn bù 2"""
    arr = tensor.contiguous().view(-1).numpy().astype(np.int32)
    with open(filename, "w") as f:
        for val in arr:
            f.write(f"{val & 0xFFFF:04x}\n")

