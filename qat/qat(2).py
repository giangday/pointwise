def load_model_state_v12(model, ckpt_path, state_keys):
    raw = torch.load(ckpt_path, map_location="cpu")
    state = extract_state_dict(raw, state_keys)
    state = maybe_strip_prefix(state, model.state_dict().keys())
    incompatible = model.load_state_dict(state, strict=False)
    return {"missing_keys": list(incompatible.missing_keys), "unexpected_keys": list(incompatible.unexpected_keys), "loaded_keys": len(state)}


def load_qat_from_baseline_v12(qat_model, baseline_encoder):
    src = baseline_encoder.state_dict()
    dst = qat_model.state_dict()
    matched = {}
    skipped = []
    for k, v in src.items():
        if k in dst and tuple(dst[k].shape) == tuple(v.shape):
            matched[k] = v
        else:
            skipped.append(k)
    dst.update(matched)
    qat_model.load_state_dict(dst, strict=False)
    return {"matched_keys": len(matched), "total_keys": len(dst), "match_ratio": 100.0 * len(matched) / max(1, len(dst)), "skipped_keys": skipped[:20]}


def run_validation_like_stegano_fix_v12(encoder, decoder, loader, metrics, device):
    encoder.eval(); decoder.eval()
    acc = {"Cov": 0.0, "Sec": 0.0, "PSNR_C": 0.0, "SSIM_C": 0.0, "PSNR_S": 0.0, "SSIM_S": 0.0}
    l1 = nn.L1Loss(); count = 0
    with torch.no_grad():
        for cover in loader:
            cover = cover.to(device, non_blocking=True)
            secret = cover[torch.randperm(cover.size(0), device=cover.device)]
            stego = encoder(cover, secret)
            recovered = decoder(stego)
            acc["Cov"] += float(l1(stego, cover).item())
            acc["Sec"] += float(l1(recovered, secret).item())
            pc, sc = metrics.calc(stego, cover)
            ps, ss = metrics.calc(recovered, secret)
            acc["PSNR_C"] += pc; acc["SSIM_C"] += sc; acc["PSNR_S"] += ps; acc["SSIM_S"] += ss
            count += 1
    return {k: (v / max(1, count)) for k, v in acc.items()}


def fine_tune_qat_like_stegano_fix_v12(encoder_qat, decoder, train_loader, val_loader, eval_pairs, metrics, device, out_dir, epochs, lr, log_interval):
    title("STEP 2 - FINE-TUNE QAT (LIKE stegano_fix_4_5.py)")
    out_dir.mkdir(parents=True, exist_ok=True)
    l1 = nn.L1Loss(); l2 = nn.MSELoss()
    optimizer = torch.optim.Adam(list(encoder_qat.parameters()) + list(decoder.parameters()), lr=lr, betas=(0.5, 0.999))
    history = []; best_score = float("inf")
    best_enc_path = out_dir / "best_enc_qat_q6_10.pth"
    best_dec_path = out_dir / "best_dec_finetuned.pth"
    for epoch in range(1, epochs + 1):
        encoder_qat.train(); decoder.train()
        running = {"Loss_G": 0.0, "Cov": 0.0, "Sec": 0.0}
        for step, cover in enumerate(train_loader, start=1):
            cover = cover.to(device, non_blocking=True)
            secret = cover[torch.randperm(cover.size(0), device=cover.device)]
            optimizer.zero_grad(set_to_none=True)
            stego = encoder_qat(cover, secret)
            recovered = decoder(stego)
            loss_cov = 1.0 * l1(stego, cover) + 0.5 * l2(stego, cover)
            loss_sec = 1.0 * l1(recovered, secret) + 0.5 * l2(recovered, secret)
            loss_g = loss_cov + loss_sec
            loss_g.backward()
            torch.nn.utils.clip_grad_norm_(list(encoder_qat.parameters()) + list(decoder.parameters()), 1.0)
            optimizer.step()
            running["Loss_G"] += float(loss_g.item()); running["Cov"] += float(loss_cov.item()); running["Sec"] += float(loss_sec.item())
            if log_interval > 0 and step % log_interval == 0:
                print(f"[FT] Ep{epoch} Step{step}/{len(train_loader)} | Loss_G={loss_g.item():.6f} | Cov={loss_cov.item():.6f} | Sec={loss_sec.item():.6f}")
        den = max(1, len(train_loader))
        val_res = run_validation_like_stegano_fix_v12(encoder_qat, decoder, val_loader, metrics, device=device)
        score = val_res["Cov"] + val_res["Sec"]
        history.append({"epoch": float(epoch), "train_loss_g": running["Loss_G"] / den, "train_cov": running["Cov"] / den, "train_sec": running["Sec"] / den, "val_cov": val_res["Cov"], "val_sec": val_res["Sec"], "val_psnr_c": val_res["PSNR_C"], "val_ssim_c": val_res["SSIM_C"], "val_psnr_s": val_res["PSNR_S"], "val_ssim_s": val_res["SSIM_S"]})
        print(f"[VAL] Epoch {epoch}/{epochs} | Score={score:.6f} | COVER PSNR={val_res['PSNR_C']:.4f} SSIM={val_res['SSIM_C']:.4f} | SECRET PSNR={val_res['PSNR_S']:.4f} SSIM={val_res['SSIM_S']:.4f}")
        evaluate_pairs_v12(encoder_qat, decoder, eval_pairs, metrics, device, out_dir / f"epoch_{epoch:02d}", f"epoch_{epoch:02d}")
        if score < best_score:
            best_score = score
            torch.save(encoder_qat.state_dict(), best_enc_path)
            torch.save(decoder.state_dict(), best_dec_path)
    hist_csv = out_dir / "finetune_history.csv"
    with hist_csv.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=list(history[0].keys())); writer.writeheader(); writer.writerows(history)
    hist_json = out_dir / "finetune_history.json"
    with hist_json.open("w", encoding="utf-8") as f:
        json.dump(history, f, indent=2, ensure_ascii=False)
    encoder_qat.load_state_dict(torch.load(best_enc_path, map_location=device), strict=True)
    decoder.load_state_dict(torch.load(best_dec_path, map_location=device), strict=True)
    return {"best_score": best_score, "best_enc_path": str(best_enc_path), "best_dec_path": str(best_dec_path), "history_csv": str(hist_csv), "history_json": str(hist_json)}


def save_weight_and_bn_info_v12(model, model_name, out_dir):
    out_dir.mkdir(parents=True, exist_ok=True)
    state_path = out_dir / f"{model_name}_state_dict.pth"
    torch.save(model.state_dict(), state_path)
    weight_rows = []; bn_rows = []
    for name, tensor in model.state_dict().items():
        t = tensor.detach().float().cpu()
        weight_rows.append({"name": name, "shape": str(list(t.shape)), "min": float(t.min().item()) if t.numel() > 0 else 0.0, "max": float(t.max().item()) if t.numel() > 0 else 0.0, "mean": float(t.mean().item()) if t.numel() > 0 else 0.0, "std": float(t.std().item()) if t.numel() > 1 else 0.0})
    for name, module in model.named_modules():
        if isinstance(module, nn.BatchNorm2d):
            bn_rows.append({"name": name, "num_features": module.num_features, "eps": module.eps, "momentum": module.momentum if module.momentum is not None else -1.0, "weight_mean": float(module.weight.detach().float().mean().item()), "bias_mean": float(module.bias.detach().float().mean().item()), "running_mean_mean": float(module.running_mean.detach().float().mean().item()), "running_var_mean": float(module.running_var.detach().float().mean().item())})
    weights_csv = out_dir / f"{model_name}_weights_summary.csv"
    with weights_csv.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=list(weight_rows[0].keys())); writer.writeheader(); writer.writerows(weight_rows)
    bn_csv = out_dir / f"{model_name}_batchnorm_summary.csv"
    if bn_rows:
        with bn_csv.open("w", newline="", encoding="utf-8") as f:
            writer = csv.DictWriter(f, fieldnames=list(bn_rows[0].keys())); writer.writeheader(); writer.writerows(bn_rows)
    return {"state_dict": str(state_path), "weights_csv": str(weights_csv), "batchnorm_csv": str(bn_csv)}


