# ==============================
# 2. FLOAT → Q6.10
# ==============================
def float_to_q6_10(x):
    """Lượng tử hóa Q6.10, kẹp biên 16-bit signed"""
    x_scaled = torch.round(x * Q_SCALE)
    x_clamped = torch.clamp(x_scaled, INT16_MIN, INT16_MAX)
    return x_clamped.to(torch.int16)

